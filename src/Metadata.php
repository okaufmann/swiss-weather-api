    /**
     * Searches metadata for a given search term
     *
     * @param $queryText
     * @return array
     */
    public function search($queryText)
    {
        $normalize = $this->removeAccents($queryText);
        $normalize = Regex::replace("/[^A-Za-z0-9\s]/", '', $normalize);
        $normalize = strtolower($normalize);
        $normalize = substr($normalize, 0, 2);

        $url = "/etc/designs/meteoswiss/ajax/search/$normalize.json";

        $result = collect($this->makeRequest($url, true));

        $result = $result->map(function ($r) {
            list($locationId, $canton, $u1, $postalCode, $u2, $cityName) = explode(';', $r);

            return [
                'location_id' => $locationId,
                'canton' => $canton,
                'postal_code' => $postalCode,
                'city_name' => $cityName,
            ];
        })->filter(function ($m) use ($queryText) {
            return str_contains($m['city_name'], $queryText) || str_contains($m['postal_code'], $queryText);
        });

        return $result;
    }

    /**
     * Returns metadata for a location.
     *
     * @param $locationId
     * @return array
     */
    public function getMetadata($locationId)
    {
        if (strlen($locationId) !== 6 || !Regex::match("/[0-9]{6}/", $locationId)->hasMatch()) {
            throw new \InvalidArgumentException('locationId must be a id out of 6 numbers');
        }

        $url = "/etc/designs/meteoswiss/ajax/location/$locationId.json";

        $result = collect($this->makeRequest($url, true));

        return [
            'location_id' => $result['location_id'],
            'city_name' => $result['city_name'],
            'postal_code' => substr($result['location_id'], 0, 4),
            'station_id' => $result['station_id'],
            'webcam_id' => $result['webcam_id'],
        ];
    }

